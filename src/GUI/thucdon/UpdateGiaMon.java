    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String update = txtShowPrice.getText();
        ThucDonDao thucdon = new ThucDonDao();
        boolean isSuccess = thucdon.UpdateGiaTheoMon(idmonan, update);
        if (isSuccess) {
            JOptionPane.showMessageDialog(this, "Cập nhật giá thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            // Cập nhật lại dữ liệu trên bảng
            if (parentForm != null) {
                parentForm.loadThucDon(new ThucDonDao().laydanhsachthucdon());
//                System.out.println("Xin chào");
            }
            // Đóng form
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Lỗi! Không thể cập nhật giá.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

