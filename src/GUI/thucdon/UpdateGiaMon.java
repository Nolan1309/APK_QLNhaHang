    private void LoadGiaUpdate(int id) {
        ThucDonDao thucdon = new ThucDonDao();
        String giacamon = thucdon.getGiaTheoMon(id);
        txtShowPrice.setText(giacamon);
    }

    private boolean isValidPrice(String price) {
        try {
            double value = Double.parseDouble(price);
            return value >= 0; // Giá phải lớn hơn hoặc bằng 0
        } catch (NumberFormatException e) {
            // Nếu không thể chuyển đổi sang số, giá không hợp lệ
            return false;
        }
    }

